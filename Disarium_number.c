#include<stdio.h>
#include<math.h>
int main()
{
    int n,orgnum,count=0,dig,sum=0;
    scanf("%d",&n);
    orgnum=n;
    while(n!=0)
    {
        count++;
        n/=10;
    }
    n=orgnum;
    while(n!=0)
    {
        dig=n%10;
        sum=pow(dig,count)+sum;
        count--;
        n/=10;
    }
    if(sum==orgnum)
    {
        printf("True");
    }
    else
    {
        printf("False");
    }
}